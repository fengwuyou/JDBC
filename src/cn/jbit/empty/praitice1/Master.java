package cn.jbit.empty.praitice1;
/**
 * 实体类
 * @author ly
 *
 */
public class Master {
	private int id;//主人编号
	private String name;//主人姓名
	private String pwd;//主人密码
	//get和set方法
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	
}
